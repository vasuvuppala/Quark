  <h1>Introduction</h1>
	 <p>Project Plan (PP) is a formal, approved document used to guide both project execution and project 
	 control. The primary uses of the project plan are to document planning assumptions and 
	 decisions, facilitate communication among stakeholders, and document approved scope, 
	 cost, and schedule baselines.  [PMBOK].
	 </p>
	 <p>Depending on the project, a project plan may be summarized or detailed. It is is the output 
	 of the <a href="proc-pln.php">Plan Project procedure</a>. A Preliminary Project Plan (PPP) is an 
	 initial version of PP that is developed during the <a href="proc-ini.php">Initiate Project</a>
	 procedure. For the rest of this document we will use the term PP to mean both Project Plan and 
	 Preliminary Project Plan.</p>
  <h1>Components</h1>
	 A PP has the following components:
	 <ul>
		<li>Project Scope: the high-level requirements of the project</li>
		<li>Project WBS: the hierarchical breakdown of the project work</li>
		<li>Project Schedule: the time-line for execution of the project</li>
		<li>Project Budget: the cost for the project, and how it is spread across the schedule</li>
		<li>Project Strategy: the high-level design or solution for the project</li>
		<li>Project Management Plans:</li>
		<ul>
			<li>Quality Plan</li>
			<li>Human Resource Plan</li>
			<li>Risk Plan</li>
			<li>Communications Plan</li>
			<li>Procurement Plan</li>
		</ul>
	</ul>
	<p>Depending on the size of the project, these components can be sections of one document, or each
	can be a set of documents.</p>
