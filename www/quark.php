<h1 align="center">Introduction</h1>      
<p><img src="images/icon-uc.png"><i>Note: Quark is still under development; it is neither 
 complete nor completely accurate.</i></p>
<p>Quark is the repository for the
 Software Process Infrastructure (SPI) of the 
 Electronics Department at <a href="http://www.nscl.msu.edu" target="_blank">NSCL</a>. 
 It contains the policies, standards, 
 procedures, guidelines, templates, tools, and checklists used during software development
 life cycle (SDLC).</p>
<p>It is based on the following standards and frameworks:</P>
<ul>
 <li><a href="http://www.sei.cmu.edu/cmmi" target="_blank">CMMI-DEV Version 1.2 </a>from 
	 <a href="http://www.sei.cmu.edu/" target="_blank">Software Engineering Institute</a> at 
	 <a href="http://www.cmu.edu" target="_blank">Carnegie Mellon University</a></li>
 <li>ISO/IEC 90003 Standard from <a href="http://www.iso.org/" target="_blank">ISO</a></li>
 <li>PMBOK 4th Edition from <a href="http://www.pmi.org" target="_blank">
	 Project Management Institute</a></li>
 <li><a href="http://www.swebok.org" target="_blank">SWEBOK</a> from <a href="http://www.ieee.org" target="_blank">IEEE</a> 
	 <a href="http://www.computer.org" target="_blank">Computer Society</a></li>
 <li><a href="http://www.agilealliance.org" target="_blank">Agile Software
	 Development</a></li>
 <li>Microsoft Solutions Framework</li>
 <li>Six Sigma and Lean Software Development</li>
</ul>
<h1 align="center">Navigation</h1>
<p>
If you are new to Quark, here is the recommended way to browse it:
</p>
<ol>
   <li> Familiarize yourself with <a href="roles.php">roles and responsibilities</a> 
		of the stakeholders.</li> 
   <li> Glance over <a href="terms.php">the terminology</a> used in Quark. Most of it is
		borrowed from standards like PMBOK, CMMI, SWEBOK etc. 
		You should revisit this section whenever a term appears to be out of context.</li>
   <li> Start with <a href="procedures.php">the top level procedures</a> 
		(PRC-DEV, PRC-MNT),
		and drill down to sub-procedures. Peruse the relevant 
		guidelines and standards referenced in each procedure. Download the
		referenced checklists and templates during the execution of your project.</li>	
   <li> Check <a href="policies.php">the policies</a> if you need guidance in 
		situations of ambiguity.</li>
</ol> 
