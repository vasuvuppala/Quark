  <tr>
	 <td width="20%">Project</td>
	 <td width="10%">&nbsp;</td>
	 <td>A temporary endeavor undertaken to create a unique product, service, or result. 
	 It has definite beginning and end.</td>
  </tr>
  <tr>
	 <td width="20%">Project Charter</td>
	 <td width="10%">&nbsp;</td>
	 <td>A document that authorizes the project, and describes its scope, objectives, 
		 and 
		 stakeholders. It also establishes the authority of the project manager. It is 
		 generally a short document of 2-5 pages.
		 </td>
  </tr>
  <tr>
	 <td width="20%">Project Management</td>
	 <td width="10%">&nbsp;</td>
	 <td>The application of knowledge, skills, tools, and techniques to meet project 
	  requirements.</td>
  </tr>
  <tr>
	 <td width="20%">Project Plan</td>
	 <td width="10%">PP</td>
	 <td>A formal, approved document used to guide both project execution and project 
	 control. The primary uses of the project plan are to document planning assumptions and 
	 decisions, facilitate communication among stakeholders, and document approved scope, 
	 cost, and schedule baselines. A project plan may be summarized or detailed [PMBOK]</td>
  </tr>
  <tr>
	 <td width="20%">Project Team</td>
	 <td width="10%">&nbsp;</td>
	 <td>Team that is involved in carrying out the project. It includes the project 
		 execution team as well
		 as project management team.</td>
  </tr>
  <tr>
	 <td width="20%">Project Management Plan</td>
	 <td width="10%">PMP</td>
	 <td>Synonym for Project Plan</td>
  </tr>
  <tr>
	 <td width="20%">Quality</td>
	 <td width="10%">&nbsp;</td>
	 <td>It is a subjective and perceptual attribute of a product, and is defined in 
		 several different ways.
		 ISO and PMBOK define it as "the degree to which a set of inherent characteristics 
		 [of a product] 
		 fulfills [its] requirements". Six Sigma has a more quantitative definition, "number of 
		 defects per million opportunities".</td>
  </tr>
   <tr>
	 <td width="20%">Quality Assurance</td>
	 <td width="10%">QA</td>
	 <td>QA is set of processes that assure that the (software) products and processes conform 
		 to their specified requirements. QA is concerned with overall process improvement. It
		 provides adequate confidence that quality is being built into the software.
		 </td>
  </tr>
   <tr>
	 <td width="20%">Quality Control</td>
	 <td width="10%">&nbsp;</td>
	 <td>Quality Control measures how the deliverables meet the quality standards. 
		 It is the process where each deliverable is inspected, measured, 
		 and tested. Whereas QA is concerned with processes, QC is with the product.</td>
  </tr>
