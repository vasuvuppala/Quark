  <tr>
	 <td width="20%">Actual Cost</td>
	 <td width="10%">AC</td>
	 <td>Total costs incurred in completing work during a given period</td>
  </tr>
  <tr>
	 <td width="20%">Architecture</td>
	 <td width="10%">&nbsp;</td>
	 <td>A description of the subsystems and components of a software 
		 system and the relationships between them. It defines the internal
		 structure of the software. It describes the way in which something 
		 is constructed or organized.</td>
  </tr>
  <tr>
	 <td width="20%">Baseline</td>
	 <td width="10%">&nbsp;</td>
	 <td>The state of an artifact against which actual performance is measured. 
		 The artifact can be Project Plan, Source Code, High Level Design etc. The baseline
		 must be approved, and includes all approved changes. It generally refers to current
		 baseline but may refer to initial or other baselines. </td>
  </tr>
  <tr>
	 <td width="20%">Budget at Completion</td>
	 <td width="10%">BAC</td>
	 <td>Sum of all the budgets established for the work to be performed on a project. The
		 total planned value of the project.</td>
  </tr>
  <tr>
	 <td width="20%">Change Control</td>
	 <td width="10%">&nbsp;</td>
	 <td>Identifying, documenting, approving or rejecting, and controlling changes to the
		 project baselines.</td>
  </tr>
  <tr>
	 <td width="20%">Change Request</td>
	 <td width="10%">&nbsp;</td>
	 <td>Request to alter the project attributes; the attributes could be scope, 
		 schedule, cost, 
		 plans, policies etc. </td>
  </tr>
  <tr>
	 <td width="20%">Configuration</td>
	 <td width="10%">&nbsp;</td>
	 <td></td>
  </tr>
  <tr>
	 <td width="20%">Cost Performance Index</td>
	 <td width="10%">CPI</td>
	 <td>Cost efficiency ratio. CPI = EV/AC</td>
  </tr>
  <tr>
	 <td width="20%">Cost Variance</td>
	 <td width="10%">CV</td>
	 <td>Cost overrun during a given period. CV = EV - AC</td>
  </tr>
  <tr>
	 <td width="20%">Defect</td>
	 <td width="10%">&nbsp;</td>
	 <td>An imperfection in a project component, where it does not meet its specifications
		 and has to be repaired or replaced. A defect can get introduced at any stage:
		 Requirement Specification, Design, Development, or Maintenance.</td>
  </tr>
  <tr>
	 <td width="20%">Deploy</td>
	 <td width="10%">&nbsp;</td>
	 <td>&nbsp;</td>
  </tr>
  <tr>
	 <td width="20%">Earned Value</td>
	 <td width="10%">EV</td>
	 <td>Work completed during a given period. It is measured in monetary value (dollars).
		 For example, if 25% of the work has been accomplished till today, for a project that has a budget
		 of $10,000.00, the EV from beginning of the project till today is $2,500.00.</td>
  </tr>
  <tr>
	 <td width="20%">Key Stakeholders</td>
	 <td width="10%">&nbsp;</td>
	 <td>Anyone in decision-making or management role who is impacted by the project outcome, such as 
		 the sponsor, the project manager, and the customer</td>
  </tr>
  <tr>
	 <td width="20%">Planned Value</td>
	 <td width="10%">PV</td>
	 <td>Work scheduled to be completed during a given period according to the project 
		 plan (project schedule). Just like EV, it is also measured in monetary terms (dollars).
		 For example, if 20% of the work was planned to be completed till today, for a project that has a budget
		 of $10,000.00, the PV from beginning of the project till today is $2,000.00.</td>
  </tr>
